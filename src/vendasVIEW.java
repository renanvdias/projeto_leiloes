    private void listarProdutos(){
        try {
            DefaultTableModel model = (DefaultTableModel) listaProdutos.getModel();
            model.setNumRows(0);
            
            ProdutosDAO produtosDAO = new ProdutosDAO();
            ArrayList<ProdutosDTO> listagemProdutos = produtosDAO.listarProdutosVendidos();
            
            for (ProdutosDTO c : listagemProdutos) { //em cada volta do la√ßo for, o mesmo adiciona uma dado(empresa) dentro do objeto c
                    Object[] obj = new Object[] { 
                        c.getId(),           
                        c.getNome(),   
                        c.getValor(),    
                        c.getStatus(),
                    };
                    model.addRow(obj);
                }    
        } catch (Exception e) {
        }
    }
    
